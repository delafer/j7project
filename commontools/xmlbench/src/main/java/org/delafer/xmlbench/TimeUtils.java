package org.delafer.xmlbench;

public class TimeUtils {
	
	public final static long MILLISECOND 	= 1l; //ms
	public final static long TERTIA 		= 17l;// 1/60sec
	public final static long SECOND 		= 1000l; //ms
	public final static long MINUTE			= SECOND * 60l; //ms
	public final static long HALF_MINUTE	= SECOND * 30l; //ms
	
	public final static long HOUR 			= MINUTE * 60l; //ms
	public final static long HALF_HOUR 		= MINUTE * 30l; //ms
	public final static long HOUR_ACADEMIC	= MINUTE * 45l; //ms
	public final static long DAY 			= HOUR * 24l; //ms
	public final static long WEEK 			= DAY * 7l; //ms
	
}
